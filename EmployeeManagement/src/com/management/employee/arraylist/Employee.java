package com.management.employee.arraylist;

public class Employee {
	private int EmpId;
	private String EmpName;
	private String EmpEmail;
	private char EmpGender;
	private float EmpSalary;

	public Employee(int empId, String empName, String empEmail, char empGender, float empSalary) {
		super();
		EmpId = empId;
		EmpName = empName;
		EmpEmail = empEmail;
		EmpGender = empGender;
		EmpSalary = empSalary;
	}

	public int getEmpId() {
		return EmpId;
	}

	public String getEmpName() {
		return EmpName;
	}

	public String getEmpEmail() {
		return EmpEmail;
	}

	public char getEmpGender() {
		return EmpGender;
	}

	public float getEmpSalary() {
		return EmpSalary;
	}

	public void getEmployeeDetails() {
		System.out.println("Employee details are:");
		System.out.println(this.EmpId+" "+this.EmpName+" "+this.EmpEmail+" "+this.EmpGender+" "+this.EmpSalary);;
	}
}
